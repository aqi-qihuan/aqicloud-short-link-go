func TestBizCodeEnum_CountByCategory(t *testing.T) {
	// 验证各分类错误码数量
	assert.Equal(t, 2, countEnums(COMMON_REPEAT_OPERATION, COMMON_NETWORK_ERROR), "Common 应有 2 个")
	assert.Equal(t, 4, countEnums(GROUP_REPEAT, GROUP_NOT_EXIST, GROUP_ADD_FAIL, GROUP_OPERATE_FAIL), "Group 应有 4 个")
	assert.Equal(t, 4, countEnums(CODE_GENERATE_ERROR, CODE_LIMITED, CODE_ERROR, CAPTCHA_ERROR), "Verification 应有 4 个")
	assert.Equal(t, 5, countEnums(ACCOUNT_REPEAT, ACCOUNT_NOT_EXIST, ACCOUNT_PWD_ERROR, ACCOUNT_UNLOGIN, ACCOUNT_TOKEN_ERROR), "Account 应有 5 个")
	assert.Equal(t, 7, countEnums(ORDER_PRICE_FAIL, ORDER_REPEAT_SUBMIT, ORDER_TOKEN_MISSING, ORDER_NOT_EXIST, ORDER_STATE_ERROR, ORDER_CANCEL_SUCCESS, ORDER_CONFIRM_SUCCESS), "Order 应有 7 个")
	assert.Equal(t, 4, countEnums(PAY_ORDER_FAIL, PAY_CALLBACK_SIGN_FAIL, PAY_ORDER_STATE_ERROR, PAY_ORDER_TIMEOUT_ERROR), "Payment 应有 4 个")
	assert.Equal(t, 3, countEnums(TRAFFIC_NOT_EXIST, TRAFFIC_REDUCE_FAIL, TRAFFIC_DATA_ERROR), "Traffic 应有 3 个")
}

func countEnums(enums ...BizCodeEnum) int {
	return len(enums)
}
