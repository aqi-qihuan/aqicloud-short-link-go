func TestBizCodeEnum_CodesUnique(t *testing.T) {
	allEnums := []BizCodeEnum{
		COMMON_REPEAT_OPERATION, COMMON_NETWORK_ERROR,
		GROUP_REPEAT, GROUP_NOT_EXIST, GROUP_ADD_FAIL, GROUP_OPERATE_FAIL,
		CODE_GENERATE_ERROR, CODE_LIMITED, CODE_ERROR, CAPTCHA_ERROR,
		ACCOUNT_REPEAT, ACCOUNT_NOT_EXIST, ACCOUNT_PWD_ERROR, ACCOUNT_UNLOGIN, ACCOUNT_TOKEN_ERROR,
		SHORT_LINK_NOT_EXIST,
		ORDER_PRICE_FAIL, ORDER_REPEAT_SUBMIT, ORDER_TOKEN_MISSING, ORDER_NOT_EXIST,
		ORDER_STATE_ERROR, ORDER_CANCEL_SUCCESS, ORDER_CONFIRM_SUCCESS,
		PAY_ORDER_FAIL, PAY_CALLBACK_SIGN_FAIL, PAY_ORDER_STATE_ERROR, PAY_ORDER_TIMEOUT_ERROR,
		DATA_OUT_OF_LIMIT_SIZE, DATA_OUT_OF_LIMIT_DATE,
		CONTROL_FLOW, CONTROL_DEGRADE, CONTROL_AUTH,
		TRAFFIC_NOT_EXIST, TRAFFIC_REDUCE_FAIL, TRAFFIC_DATA_ERROR,
		FILE_UPLOAD_ERROR,
		DB_ROUTING_ERROR,
		MQ_CONSUME_ERROR,
	}

	seen := make(map[int]string)
	for _, e := range allEnums {
		if prev, exists := seen[e.Code()]; exists {
			t.Errorf("错误码 %d 重复，出现在 %s 和 %s", e.Code(), prev, e.Message())
		}
		seen[e.Code()] = e.Message()
	}
}

